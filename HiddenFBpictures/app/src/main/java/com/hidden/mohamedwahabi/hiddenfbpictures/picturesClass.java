public class picturesClass {
    String id,urlpic;

    public picturesClass(String id, String urlpic) {
        this.id = id;
        this.urlpic = urlpic;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUrlpic() {
        return urlpic;
    }

    public void setUrlpic(String urlpic) {
        this.urlpic = urlpic;
    }
}
